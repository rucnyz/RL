from typing import Any

from datasets import load_dataset

from nemo_rl.data.datasets.raw_dataset import RawDataset


class OpenSageDataset(RawDataset):
    """Dataset for OpenSage Harbor tasks.

    Loads JSONL with {prompt, task_id, task_dir} from prepare_harbor_prompts.py.
    Only needs the prompt (user message) — no output/assistant response required,
    since the model generates responses during RL rollout.

    Args:
        data_path: Path to the JSONL file generated by prepare_harbor_prompts.py
        input_key: Key for the prompt text, default is "prompt"
        split_validation_size: Size of the validation data, default is 0
        seed: Seed for train/validation split, default is 42
    """

    def __init__(
        self,
        data_path: str,
        input_key: str = "prompt",
        split_validation_size: float = 0,
        seed: int = 42,
        **kwargs,
    ):
        self.input_key = input_key
        self.task_name = "opensage"

        self.dataset = load_dataset("json", data_files=data_path, split="train")
        self.dataset = self.dataset.map(
            self.format_data,
            remove_columns=self.dataset.column_names,
        )

        self.val_dataset = None
        self.split_train_validation(split_validation_size, seed)

    def format_data(self, data: dict[str, Any]) -> dict[str, Any]:
        return {
            "messages": [
                {"role": "user", "content": data[self.input_key]},
            ],
            "extra_env_info": {
                "task_id": data.get("task_id", ""),
                "task_dir": data.get("task_dir", ""),
            },
            "task_name": self.task_name,
        }
